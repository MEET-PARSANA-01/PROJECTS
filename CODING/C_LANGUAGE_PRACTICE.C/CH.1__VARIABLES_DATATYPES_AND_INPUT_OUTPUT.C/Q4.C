// Create a program to declare two integer variables, assign them values, and display their values.
#include <stdio.h>
int main()
{
  int n, m;
  printf("WELCOME TO DECLARE AND ASSIGN VALUES OF INT VARIABLES!\n");
  printf("ENTER THE VALUE OF FIRST VARIABLE: ");
  scanf("%d", &n);
  printf("NOW,ENTER THE VALUE OF SECOND VARIABLE: ");
  scanf("%d", &m);

  printf("THE VALUE OF VARIABLES M AND N VARIABLES RESPECTIVELY ARE %d AND %d", n, m);

  return 0;
}